-- Тест l (вправо) на строке "a <= b":
--   Реально:   a <= b
--   Визуально: a ≤ b
--   Позиции:   0123456
--
--   1. Курсор на 'a' (0), l -> ' ' (1)        [обычное движение]
--   2. Курсор на ' ' (1), l -> '<' (2)        [обычное движение на начало символа]
--   3. Курсор на '<' (2), l -> ' ' (4)        [перескочил через <= вправо]
--   4. Курсор на '=' (3), l -> ' ' (4)        [перескочил из середины символа вправо]
--
-- Тест h (влево) на строке "a <= b":
--   1. Курсор на 'b' (5), h -> ' ' (4)        [обычное движение]
--   2. Курсор на ' ' (4), h -> '<' (2)        [перескочил на начало <=]
--   3. Курсор на '=' (3), h -> '<' (2)        [к началу символа]

