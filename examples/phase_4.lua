--   foo: 0-2, ->: 4-5, bar: 7-9, ->: 11-12, baz: 14-16
--
-- Тест w (вперёд к началу слова):
--   1. Курсор на 'f' (0), w -> '-' (4)    [к началу первого ->]
--   2. Курсор на '-' (4), w -> 'b' (7)    [через -> к началу bar]
--   3. Курсор на 'b' (7), w -> '-' (11)   [к началу второго ->]
--   4. Курсор на '-' (11), w -> 'b' (14)  [через -> к началу baz]
--
-- Тест b (назад к началу слова):
--   1. Курсор на 'b' (14), b -> '-' (11)  [к началу второго ->]
--   2. Курсор на '-' (11), b -> 'b' (7)   [к началу bar]
--   3. Курсор на 'b' (7), b -> '-' (4)    [к началу первого ->]
--   4. Курсор на '-' (4), b -> 'f' (0)    [к началу foo]
--
-- Тест e (к концу слова):
--   1. Курсор на 'f' (0), e -> 'o' (2)    [к концу foo]
--   2. Курсор на 'o' (2), e -> '-' (4)    [к началу ->, визуально "на" →]
--   3. Курсор на '-' (4), e -> 'r' (9)    [к концу bar]
--   4. Курсор на 'r' (9), e -> '-' (11)   [к началу ->, визуально "на" →]

-- Ещё примеры для тестирования:
local chain = a <= b >= c ~= d
